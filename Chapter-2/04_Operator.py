# Arithmetic Operator

a = 34

b = 45

c = a + b

print(c)

# Assignment Operator

d = 6 # Assign 6 in d
d += 3 # increment the value of d by 3 and then assign it to b (same ex: -=, *=, /=)
print(d)

# Comparison Operator (return Boolean Value)
# Ex : >, <, <=, >=, ==, !=
e = 5>4 
print(e)

# Logical Operator

# Truth Table of 'or'

print("True or False is: ", True or False)
print("True or True is: ", True or True)
print("False or True is: ", False or True)
print("Fasle or False is: ", False or False)

# Truth Table of 'and'

print("True and False is: ", True and False)
print("True and True is: ", True and True)
print("False and True is: ", False and True)
print("Fasle and False is: ", False and False)

# not Operator

print(not(False)) # ans : True

